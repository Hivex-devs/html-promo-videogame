function renderMap() {
  var lastArr;
  isRender = true;

  if (!!$.cookie('coorX')) {
    coorX = parseInt($.cookie('coorX'));
    coorY = parseInt($.cookie('coorY'));

  }

  if (!!$.cookie('count')) {
    let lastCount = parseInt($.cookie('count'))
    let resCount = lastCount < 10 ? '00' + lastCount : '0' + lastCount;

    $('.count_in p').text(resCount);

    $('.count_in span').each(function (item) {
      if (item < lastCount) {
        $(this).addClass('active')
      }
    })
  }


  $.each(map, function (y, item) {
    $.each(item, function (i, item) {
      if (("" + item).indexOf('4') > -1) {
        $('#area')
          .css({
            width: map[0].length * (sizeUser[0] / 2) + "px",
            height: map.length * (sizeUser[0] / 2) + "px",
          })
          .append('<i style="top: ' + y * sizeUser[0] / 2 + 'px; left: ' + i * sizeUser[1] / 2 + 'px; width: ' + sizeUser[0] / 2 + 'px; height: ' + sizeUser[1] / 2 + 'px;" class="grid type-' + item + '"></i>')
      }

    })
  })
  $('#area').addClass('show').width(map[0].length * sizeUser[1] / 2);
  $('.lazy').each(function () {
    $(this).attr('src', $(this).data('src'))
  })
  $('#area').height(map.length * sizeUser[0] / 2);
  $('#area').append('<i class="grid icon-user me-down"></i>');


  if (!!$.cookie('map')) {
    let json_str = $.cookie('map');
    let lastArr = JSON.parse(json_str);
    let isActive;

    $.each(lastArr, function (conut, arr) {
      isActive = map[arr[0]][arr[1]][0] + map[arr[0]][arr[1]][1]
      $('.grid').each(function () {
        if ($(this).attr('class').indexOf('type-' + isActive) > -1) {
          $(this).removeClass();
          map[arr[0]][arr[1]] = 1;
        }
      });
      $.each(map, function (y, item) {
        $.each(item, function (i, item) {
          if (("" + item).indexOf(isActive) > -1) {
            map[y][i] = 1;
          }
        })
      })
    })


  }


  $('.icon-user').css({
    width: sizeUser[1] / 2,
    height: sizeUser[0] / 2,
    top: coorY * sizeUser[1] / 2,
    left: coorX * sizeUser[0] / 2
  })


}

