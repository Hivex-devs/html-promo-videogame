function initNav() {
  let i = 0, timeOut = 0;

  $(document).on('keydown', function (e) {
    if (e.keyCode === 13 || e.keyCode === 32) {
      if (!isHasPopup) {
        if (!isRender) {
        }
        $('.done, .share-popup').removeClass('open');
        $.fancybox.close();
      }
      else {
        $('.btb-action').trigger('click')
      }

    }

    map[coorY][coorX] = 1;
    if (e.keyCode === 40) {
      renderMapNew(coorX, coorY + 1, 'down');
    }
    if (e.keyCode === 38) {
      renderMapNew(coorX, coorY - 1, "up");
    }
    if (e.keyCode === 37) {
      renderMapNew(coorX - 1, coorY, "left");
    }
    if (e.keyCode === 39) {
      renderMapNew(coorX + 1, coorY, "right");
    }
  })

  $(document).on('keyup', function (e) {
    $('.icon-user').removeClass('move1 move2')
  });

  $('.nav span').off().on('click', function (e) {
    $('.nav .info').fadeOut();
    var _th = $(this);
    map[coorY][coorX] = 1;
    switch (_th.attr('id')) {
      case 'up':
        renderMapNew(coorX, coorY - 1, 'up');
        break;
      case 'down':
        renderMapNew(coorX, coorY + 1, "down");
        break;
      case 'left':
        renderMapNew(coorX - 1, coorY, "left");
        break;
      case 'right':
        renderMapNew(coorX + 1, coorY, "right");
        break;
      default:
        break;

    }
  })

  $('.nav span').off().on('mousedown touchstart', function (e) {
    $('.nav .info').fadeOut();
    var _th = $(this);
    timeOut = setInterval(function () {
      map[coorY][coorX] = 1;
      switch (_th.attr('id')) {
        case 'up':
          renderMapNew(coorX, coorY - 1, 'up');
          break;
        case 'down':
          renderMapNew(coorX, coorY + 1, "down");
          break;
        case 'left':
          renderMapNew(coorX - 1, coorY, "left");
          break;
        case 'right':
          renderMapNew(coorX + 1, coorY, "right");
          break;
        default:
          break;

      }
    }, 100);
  }).bind('mouseup mouseleave touchend', function () {
    clearInterval(timeOut);
    $('.icon-user').removeClass('move1 move2')
  });

}

function moveScreen(type) {
  switch (type) {
    case 'up':
      countScrollY--;
      break;
    case 'down':
      countScrollY++;
      break;
    case 'left':
      countScrollX--;
      break;
    case 'right':
      countScrollX++;
      break;
    default:
      break;

  }

  $('#area').css({
    'marginTop': -(countScrollY * sizeUser[1] / 2),
    'marginLeft': -(countScrollX * sizeUser[0] / 2),
  });
}

function renderMapNew(x, y, type) {
  if (y > -1 && x > -1 && map[0].length > x && map.length > y) {
    if (map[y][x] === 1) {
      let lastClass = $('.icon-user').hasClass('move1');
      if (lastClass) {
        $('.icon-user').toggleClass('move2 move1')
      }
      else {
        $('.icon-user').addClass('move1')
      }
      initAboutModal(x, y);
      moveScreen(type);
      coorX = x;
      coorY = y;
      map[y][x] = 2;
      $('.icon-user').removeClass('me-down me-up me-right me-left').addClass('me-' + type).css({
        top: coorY * sizeUser[1] / 2,
        left: coorX * sizeUser[0] / 2
      })

      $.cookie('coorX', coorX, {
        expires: date,
      });
      $.cookie('coorY', coorY, {
        expires: date,
      });


    }
  }

}

function initAboutModal(x, y) {
  if (('' + map[y][x]).indexOf('4') > -1 || ('' + map[y - 1][x]).indexOf('4') > -1 || ('' + map[y + 1][x]).indexOf('4') > -1 || ('' + map[y][x - 1]).indexOf('4') > -1 || ('' + map[y][x + 1]).indexOf('4') > -1) {
    isHasPopup = true;

    var getIdEl = ('' + map[y - 1][x]).indexOf('4') > -1 ? [(y - 1), x] : ('' + map[y + 1][x]).indexOf('4') > -1 ? [(y + 1), x] : ('' + map[y][x - 1]).indexOf('4') > -1 ? [y, (x - 1)] : ('' + map[y][x + 1]).indexOf('4') > -1 ? [y, (x + 1)] : [y, x];
    $('.btb-action').fadeIn().attr('data-src', '#' + map[getIdEl[0]][getIdEl[1]])

  } else {
    isHasPopup = false;
    $('.btb-action').fadeOut().attr('data-src', '')
  }
}

