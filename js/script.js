function openModal() {
  var isActive;
  let isArrCoor = [];
  $('.btb-action').on('click', function () {
    let x = coorX;
    let y = coorY;
    let resVal = parseInt($('.count_in p').text()) + 1;
    let resCount = resVal < 10 ? '00' + resVal : '0' + resVal;
    var getIdEl = ('' + map[y - 1][x]).indexOf('4') > -1 ? [(y - 1), x] : ('' + map[y + 1][x]).indexOf('4') > -1 ? [(y + 1), x] : ('' + map[y][x - 1]).indexOf('4') > -1 ? [y, (x - 1)] : ('' + map[y][x + 1]).indexOf('4') > -1 ? [y, (x + 1)] : [y, x];

    isActive = map[getIdEl[0]][getIdEl[1]][0] + map[getIdEl[0]][getIdEl[1]][1]

    isArrCoor.push(getIdEl)

    $('.grid').each(function () {
      if ($(this).attr('class').indexOf('type-' + isActive) > -1) {
        $(this).removeClass();
        map[getIdEl[0]][getIdEl[1]] = 1;
      }
    });

    $.each(map, function (y, item) {
      $.each(item, function (i, item) {
        if (("" + item).indexOf(isActive) > -1) {
          map[y][i] = 1;
        }
      })
    })
    if (('' + map[y - 1][x]).indexOf('4') > -1) {
      map[y - 1][x] = 1;
    }
    if (('' + map[y + 1][x]).indexOf('4') > -1) {
      map[y + 1][x] = 1;
    }
    if (('' + map[y][x - 1]).indexOf('4') > -1) {
      map[y][x - 1] = 1;
    } else {
      map[y][x + 1] = 1;
    }

    if ($('.count_in .active').length) {
      $('.count_in .active').next().addClass('active')
    } else {
      $('.count_in span').first().addClass('active')
    }

    $('.count_in p').text(resCount)
    setTimeout(function () {
      $('.btb-action').fadeOut().attr('data-src', '')
    }, 100)

    $.cookie('count', $('.count_in .active').length, {
      expires: date,
    });


    if (!!$.cookie('map')) {
      let json_str = $.cookie('map');
      let lastArr = JSON.parse(json_str);
      isArrCoor = isArrCoor.concat(lastArr)
    }

    setTimeout(function () {
      $.cookie('map', JSON.stringify(isArrCoor), {
        expires: date,
      });
    }, 1)
  })


}

